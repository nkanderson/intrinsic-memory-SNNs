"""Validate FPGA inference against golden vectors.

Feeds each golden vector (generated by golden_vectors.py) to the FPGA over
UART, records the returned action, and compares against expected_action.

Mismatch categories:
  protocol        -- ProtocolError or TimeoutError during the inference round-trip
  action_mismatch -- FPGA returned a different action than the reference model
                     (likely a synthesis, timing, or host-encoding issue)

Usage:
    python validate_fpga.py --golden path/to/lif-64-16.json --port /dev/ttyUSB1
"""
from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import Any

from fpga_interface import FpgaInterface, ProtocolError


def _classify(mismatch: dict[str, Any]) -> str:
    return mismatch["category"]


def main() -> int:
    parser = argparse.ArgumentParser(description="Validate FPGA against golden vectors")
    parser.add_argument("--golden", required=True, help="Path to golden vectors JSON")
    parser.add_argument("--port", default="/dev/ttyUSB1")
    parser.add_argument("--baud", type=int, default=115_200)
    parser.add_argument("--timeout", type=float, default=1.0,
                        help="Per-operation serial timeout (s)")
    parser.add_argument("--limit", type=int, default=0,
                        help="Stop after this many vectors (0 = all)")
    args = parser.parse_args()

    golden = json.loads(Path(args.golden).read_text())
    vectors: list[dict[str, Any]] = golden["vectors"]
    if args.limit > 0:
        vectors = vectors[: args.limit]

    total = len(vectors)
    matches = 0
    mismatches: list[dict[str, Any]] = []

    with FpgaInterface(args.port, args.baud, timeout=args.timeout) as fpga:
        fpga.ping()
        model_name = golden.get("model", "unknown")
        print(f"PING OK — model={model_name}  vectors={total}  port={args.port}")

        for i, vec in enumerate(vectors):
            obs: list[int] = vec["obs_qs213"]
            expected: int = vec["expected_action"]

            try:
                fpga.write_obs(obs)
                fpga.start_inference()
                fpga.wait_done(timeout_s=args.timeout)
                actual = fpga.read_action()
            except (ProtocolError, TimeoutError) as exc:
                mismatches.append({"index": i, "category": "protocol", "detail": str(exc)})
                print(f"  [{i:5d}] PROTOCOL  {exc}")
                continue

            if actual == expected:
                matches += 1
            else:
                mismatches.append({
                    "index": i,
                    "category": "action_mismatch",
                    "expected": expected,
                    "actual": actual,
                    "obs_qs213": obs,
                })
                print(f"  [{i:5d}] MISMATCH  expected={expected}  actual={actual}  obs={obs}")

            if (i + 1) % 50 == 0:
                pct = matches / (i + 1)
                print(f"  progress {i + 1}/{total}: {pct:.1%} match")

    # Summary
    match_rate = matches / total if total else 0.0
    print(f"\nResult: {matches}/{total} matched ({match_rate:.2%})")
    if mismatches:
        by_cat: dict[str, int] = {}
        for m in mismatches:
            by_cat[m["category"]] = by_cat.get(m["category"], 0) + 1
        for cat, count in sorted(by_cat.items()):
            print(f"  {cat}: {count}")

    return 0 if not mismatches else 1


if __name__ == "__main__":
    raise SystemExit(main())
